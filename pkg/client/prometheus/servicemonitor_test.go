package prometheus

import (
	monclientv1 "github.com/coreos/prometheus-operator/pkg/client/versioned/typed/monitoring/v1"
	"reflect"
	"testing"

	monv1 "github.com/coreos/prometheus-operator/pkg/apis/monitoring/v1"
	monfake "github.com/coreos/prometheus-operator/pkg/client/versioned/fake"

	metav1 "k8s.io/apimachinery/pkg/apis/meta/v1"
)

func Test_serviceMonitor_List(t *testing.T) {
	sm1 := monv1.ServiceMonitor{
		ObjectMeta: metav1.ObjectMeta{
			Name:      "sm1",
			Namespace: "test",
		},
	}

	sm2 := monv1.ServiceMonitor{
		ObjectMeta: metav1.ObjectMeta{
			Name:      "sm2",
			Namespace: "test",
		},
	}

	sm3 := monv1.ServiceMonitor{
		ObjectMeta: metav1.ObjectMeta{
			Name:      "sm3",
			Namespace: "test1",
		},
	}

	type fields struct {
		prometheusCli monclientv1.MonitoringV1Interface
	}
	type args struct {
		namespace string
	}
	tests := []struct {
		name    string
		fields  fields
		args    args
		want    *monv1.ServiceMonitorList
		wantErr bool
	}{
		{
			"ListServiceMonitor",
			fields{
				prometheusCli: monfake.NewSimpleClientset(&sm1, &sm2, &sm3).MonitoringV1(),
			},
			args{
				"test",
			},
			&monv1.ServiceMonitorList{
				Items: []*monv1.ServiceMonitor{
					&sm1,
					&sm2,
				},
			},
			false,
		},
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			s := &serviceMonitor{
				prometheusCli: tt.fields.prometheusCli,
			}
			got, err := s.List(tt.args.namespace)
			if (err != nil) != tt.wantErr {
				t.Errorf("List() error = %v, wantErr %v", err, tt.wantErr)
				return
			}
			if !reflect.DeepEqual(got, tt.want) {
				t.Errorf("List() got = %v, want %v", got, tt.want)
			}
		})
	}
}
